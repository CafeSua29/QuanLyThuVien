    private void btnSuaSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaSachActionPerformed
        index = listSach.getSelectedRow();
        int check = list.size();
        if(check == 0){
            JOptionPane.showMessageDialog(rootPane, "Thư viện không co sách để sửa !");
        } else if (index < 0){
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn sách để sửa !");
        } else {
            SuaSach suasach = new SuaSach(this, rootPaneCheckingEnabled);
            suasach.setEditData(list.get(index));
            suasach.setVisible(true);
        }
    }//GEN-LAST:event_btnSuaSachActionPerformed

