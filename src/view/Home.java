    public void getSelectedSach(Sach sach) {
        list = new DAO().getSelectedSach(sach);
        model.setRowCount(0);
        for (Sach sach1 : list){
            model.addRow(new Object[]{
            sach1.getMaSach(), sach1.getTenSach(), sach1.getTenTacGia(), sach1.getTheLoai(), sach1.getHienTrang(), sach1.getSoLuong()
        });
        }    
    }
