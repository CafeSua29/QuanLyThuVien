    private void btnMuonSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuonSachActionPerformed
        index = listSelectedSach.getSelectedRow();
 
        if (index < 0){
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn sách để mượn !");
        } else if (list.get(index).getSoLuong() <= 0){
            JOptionPane.showMessageDialog(rootPane, "Sách đã được mượn hết !");
        } else {
            for (int i = 0; i < listSelectedSach.getRowCount(); i++) {
            Boolean chek = Boolean.valueOf(listSelectedSach.getValueAt(i, 6).toString());
            if (chek){
                int masach = Integer.parseInt(listSelectedSach.getValueAt(i, 0).toString());               
                Sach sach = new DAO().TimSach(masach);
                
                if(new DAO().MuonSach(sach, User)){
                    int soluong = 0;
                
                    soluong = new DAO().getSoLuongSach(sach) - 1;
                
                    if(new DAO().setSoLuongSach(sach, soluong)){
                        int luotmuon = 0;
                    
                        luotmuon = new DAO().getLuotMuon(sach) + 1;
                    
                        if(new DAO().setLuotMuon(sach, luotmuon)){

                        }                   
                    }
                }                                
            }                      
        }
            JOptionPane.showMessageDialog(rootPane, "Mượn thành công !");
        
            showTable();
            homesv.showTable();         
        }
    }//GEN-LAST:event_btnMuonSachActionPerformed

